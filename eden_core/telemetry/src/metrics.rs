impl<S, B> actix_web::dev::Service<actix_web::dev::ServiceRequest> for MetricsMiddlewareService<S>
where
    S: actix_web::dev::Service<actix_web::dev::ServiceRequest, Response = actix_web::dev::ServiceResponse<B>, Error = actix_web::Error>
        + 'static,
    S::Future: 'static,
    B: actix_web::body::MessageBody + 'static,
    B::Error: actix_web::ResponseError, // Add this constraint
{
    type Response = actix_web::dev::ServiceResponse<actix_web::body::EitherBody<B>>; // Change this
    type Error = actix_web::Error;
    type Future = Pin<Box<dyn Future<Output = Result<Self::Response, Self::Error>>>>;

    fn poll_ready(&self, ctx: &mut core::task::Context<'_>) -> std::task::Poll<Result<(), Self::Error>> {
        self.service.poll_ready(ctx)
    }
    #[named]
    fn call(&self, req: actix_web::dev::ServiceRequest) -> Self::Future {
        // Clone what we need for the async block
        let metrics_clone = match req.app_data::<Data<AllMetrics>>() {
            Some(metrics) => metrics.clone(),
            None => {
                let fut = self.service.call(req);
                return Box::pin(async move { fut.await.map(|res| res.map_into_left_body()) });
            }
        };

        let (start, mut telemetry_wrapper, user, request_length) = self.start_request(&req, metrics_clone.clone());

        // Record eden request start time for duration tracking
        telemetry_wrapper.mut_durations(|durations| durations.set_eden_request(start));

        let mut span = telemetry_wrapper.server_tracer("eden.http.request".to_string());
        span.set_attribute("component", "http");
        span.set_attribute("http.method", req.method().as_str().to_string());
        span.set_attribute("http.route", req.path().to_string());
        span.set_attribute(LABEL_TRAFFIC_CLASS, telemetry_wrapper.labels().traffic_class().to_string());
        let trace_id = span.trace_id().to_string();
        let span_id = span.span_id().to_string();
        let response_traceparent = span.traceparent();
        span.enter(); // Set as current span for logging

        // Check if client wants verbose response
        let wants_verbose = req.query_string().contains("verbose=true")
            || req.headers().get(HEADER_VERBOSE).and_then(|h| h.to_str().ok()).map(|v| v == "true").unwrap_or(DEFAULT_VERBOSE);

        // Create metadata for verbose mode
        let metadata = if wants_verbose {
            Some(RequestMetadata {
                user_id: user.clone(),
                request_id: uuid::Uuid::new_v4().to_string(),
                request_start: start,
                client_info: req.headers().get("user-agent").and_then(|h| h.to_str().ok()).map(|s| s.to_string()),
                request_size: request_length,
                endpoint: req.path().to_string(),
                method: req.method().to_string(),
            })
        } else {
            None
        };

        // Store metadata in request extensions if verbose
        if let Some(ref meta) = metadata {
            req.extensions_mut().insert(meta.clone());
        }

        let mut extensions = req.extensions_mut();
        extensions.insert(telemetry_wrapper.labels().clone());
        extensions.insert(telemetry_wrapper.durations().clone());
        extensions.insert(telemetry_wrapper.metadata_map().clone());
        let telemetry_data = web::Data::new(RwLock::new(telemetry_wrapper));
        extensions.insert(telemetry_data.clone());
        // Store trace IDs for downstream use
        extensions.insert((trace_id.clone(), span_id.clone()));

        drop(extensions);
        let _ctx = ctx_with_trace!().with_feature("telemetry");

        let service_fut = self.service.call(req);
        Box::pin(async move {
            // Keep the request span alive until the wrapped service completes.
            let _request_span = span;

            // Extract trace IDs and update labels BEFORE cloning
            let labels_clone = {
                let mut tw = telemetry_data.write().await;

                // Update labels with trace IDs captured at span creation
                tw.mut_labels(|labels| {
                    labels.set_trace_ids(trace_id, span_id);
                });

                let labels = tw.labels_low_cardinality();
                log_trace!(
                    _ctx.clone(),
                    "Metrics -- client tracer initialized",
                    audience = eden_logger_internal::LogAudience::Internal
                );

                labels
            };

            // Helper to convert owned labels to refs
            fn labels_to_refs(labels: &[(String, String)]) -> Vec<(&str, &str)> {
                labels.iter().map(|(k, v)| (k.as_str(), v.as_str())).collect()
            }

            let labels_refs = labels_to_refs(&labels_clone);
            metrics_clone.eden.start_request(user.clone(), request_length, &labels_refs).await;
            log_trace!(
                _ctx.clone(),
                "Metrics -- request started with labels",
                audience = eden_logger_internal::LogAudience::Internal,
                labels = format!("{:?}", labels_clone)
            );
            let endpoint_start = Utc::now();
            let _endpoint_guard = EndpointGuard::new(&metrics_clone.endpoint, &labels_refs);
            drop(labels_refs);
            let active_labels_clone = labels_clone;

            // Handle the future result
            match service_fut.await {
                Ok(service_response) => {
                    let endpoint_end = Utc::now();
                    let end = endpoint_end;

                    let (request, http_response) = service_response.into_parts();

                    let status = http_response.status();
                    let headers = http_response.headers().clone();

                    let (mut http_response, body) = http_response.into_parts();

                    let labels_clone = {
                        let (opt_labels, opt_durations) = {
                            let extensions_mut = http_response.extensions_mut();
                            (
                                extensions_mut.get::<TelemetryLabels>().cloned(),
                                extensions_mut.get::<TelemetryDurations>().cloned(),
                            )
                        };

                        if opt_labels.is_some() || opt_durations.is_some() {
                            let mut tw = telemetry_data.write().await;
                            if let Some(labels) = opt_labels {
                                log_trace!(
                                    _ctx.clone(),
                                    "Metrics -- service responded with labels",
                                    audience = eden_logger_internal::LogAudience::Internal,
                                    labels = format!("{:?}", labels)
                                );
                                tw.labels = labels;
                            }
                            if let Some(handler_durations) = opt_durations {
                                log_trace!(
                                    _ctx.clone(),
                                    "Metrics -- service responded with durations",
                                    audience = eden_logger_internal::LogAudience::Internal,
                                    durations = format!("{:?}", handler_durations)
                                );

                                // Merge durations intelligently: preserve eden_request/eden_response from middleware,
                                // but allow handlers to set endpoint_request/endpoint_response
                                tw.durations.merge_from_handler(handler_durations);
                            }

                            // If handler didn't set endpoint duration, set it explicitly from middleware timing
                            if tw.durations.get_endpoint_duration().is_none() {
                                tw.durations.set_endpoint_request(endpoint_start);
                                tw.durations.set_endpoint_response(endpoint_end);
                            }

                            tw.labels_low_cardinality()
                        } else {
                            // No handler updates, but still set endpoint duration if not set
                            let mut tw = telemetry_data.write().await;
                            if tw.durations.get_endpoint_duration().is_none() {
                                tw.durations.set_endpoint_request(endpoint_start);
                                tw.durations.set_endpoint_response(endpoint_end);
                            }
                            tw.labels_low_cardinality()
                        }
                    };
                    // finish endpoint request

                    // Skip verbose mode for streaming responses (SSE, etc.)
                    let is_streaming_response = headers
                        .get("content-type")
                        .and_then(|h| h.to_str().ok())
                        .map(|ct| ct.contains("text/event-stream") || ct.contains("text/plain"))
                        .unwrap_or(false);

                    if wants_verbose && !is_streaming_response {
                        let durations = {
                            let mut tw = telemetry_data.write().await;
                            tw.set_eden_request_end(end);
                            tw.mut_labels(|labels| {
                                labels.set_http_status(status);
                                // Note: trace IDs already set in async_block initialization
                            });
                            tw.durations.clone()
                        };

                        let metadata = match metadata {
                            Some(m) => m,
                            None => {
                                let mut response_builder = HttpResponse::build(status);
                                for (name, value) in headers.iter() {
                                    response_builder.insert_header((name.clone(), value.clone()));
                                }
                                insert_traceparent_header(&mut response_builder, &response_traceparent);
                                let http_response = response_builder.body(body);
                                let service_response = ServiceResponse::new(request, http_response).map_into_right_body();
                                return Ok(service_response);
                            }
                        };

                        let size = if let BodySize::Sized(size) = body.size() { size } else { 0 };

                        // Use pre-calculated durations from telemetry_data (optimized approach)
                        let eden_duration = durations.eden_duration.unwrap_or_default().as_duration();
                        let endpoint_duration = durations.endpoint_duration.unwrap_or_default().as_duration();
                        let exclusive_duration = durations.exclusive_duration.unwrap_or_default().as_duration();

                        // Calculate for JSON metadata compatibility
                        let total_duration_ms = eden_duration.num_milliseconds();
                        let total_duration_us = eden_duration.num_microseconds().unwrap_or(0);
                        let endpoint_processing_time_ms = endpoint_duration.num_milliseconds();
                        let endpoint_processing_time_us = endpoint_duration.num_microseconds().unwrap_or(0);
                        let exclusive_duration_ms = exclusive_duration.num_milliseconds();
                        let exclusive_duration_us = exclusive_duration.num_microseconds().unwrap_or(0);

                        // Get user info
                        let (org_uuid, user_id_val) = user.parts();
                        let (org_uuid_str, user_id_str) = (org_uuid.to_string(), user_id_val.to_string());

                        // Create comprehensive metadata
                        let response_metadata = serde_json::json!({
                            "request": {
                                "id": metadata.request_id,
                                "method": metadata.method,
                                "endpoint": metadata.endpoint,
                                "size_bytes": metadata.request_size,
                                "user_id": {
                                    "org_uuid": org_uuid_str,
                                    "user_id": user_id_str
                                },
                                "client_info": metadata.client_info
                            },
                            "response": {
                                "status": status.as_u16(),
                                "size_bytes": size,
                                "content_type": headers
                                    .get("content-type")
                                    .and_then(|h| h.to_str().ok())
                                    .unwrap_or("unknown")
                            },
                            "cache": {
                                //TODO collect cache data from req headers
                            },
                            "timing": {
                                "start_time": metadata.request_start,
                                "end_time": end,
                                "total_request_duration_ms": total_duration_ms,
                                "total_processing_time_us": total_duration_us,
                                "eden_processing_time_ms": total_duration_ms,
                                "eden_processing_time_us": total_duration_us,
                                "endpoint_processing_time_ms": endpoint_processing_time_ms,
                                "endpoint_processing_time_us": endpoint_processing_time_us,
                                "exclusive_duration_ms": exclusive_duration_ms,
                                "exclusive_duration_us": exclusive_duration_us,
                            },
                            "server": {
                                "hostname": std::env::var("HOSTNAME").unwrap_or_else(|_| "unknown".to_string()),
                                "version": std::env::var("APP_VERSION").unwrap_or_else(|_| "dev".to_string()),
                            }
                        });

                        let is_streaming = headers
                            .get("content-type")
                            .and_then(|value| value.to_str().ok())
                            .map(|value| value.starts_with("text/event-stream"))
                            .unwrap_or(false);

                        if is_streaming {
                            telemetry_data.write().await.mut_durations(|durations| durations.set_eden_response(end));

                            let response_length = body_size_or_content_length(&body, &headers);

                            let mut durations = telemetry_data.read().await.durations.clone();
                            let is_error = status.is_client_error() || status.is_server_error();
                            let labels_refs = labels_to_refs(&labels_clone);
                            let active_labels_refs = labels_to_refs(&active_labels_clone);
                            metrics_clone.eden.complete_request_with_active_labels(
                                response_length,
                                end,
                                &mut durations,
                                is_error,
                                &labels_refs,
                                &active_labels_refs,
                            );
                            telemetry_data.write().await.durations = durations;

                            let mut response_builder = HttpResponse::build(status);
                            for (name, value) in headers.iter() {
                                response_builder.insert_header((name.clone(), value.clone()));
                            }
                            insert_traceparent_header(&mut response_builder, &response_traceparent);
                            let http_response = response_builder.body(body);
                            let service_response = ServiceResponse::new(request, http_response).map_into_right_body();

                            return Ok(service_response);
                        }

                        let body_bytes = match actix_web::body::to_bytes(body).await {
                            Ok(bytes) => bytes,
                            Err(e) => return Err(actix_web::Error::from(e)),
                        };

                        // Try to parse existing JSON response and add metadata
                        let modified_response = match serde_json::from_slice::<Value>(&body_bytes) {
                            Ok(mut json_value) => {
                                // If it's a JSON object, add metadata field
                                if let Value::Object(ref mut map) = json_value {
                                    map.insert("_meta".to_string(), response_metadata);
                                } else {
                                    // If response isn't an object, wrap it
                                    json_value = serde_json::json!({
                                        "data": json_value,
                                        "_meta": response_metadata
                                    });
                                }
                                json_value
                            }
                            Err(_) => {
                                // Not JSON, create a new JSON wrapper
                                serde_json::json!({
                                    "data": String::from_utf8_lossy(&body_bytes),
                                    "_meta": response_metadata,
                                    "_note": "Original response was not JSON - converted to string"
                                })
                            }
                        };

                        let response_body = serde_json::to_vec_pretty(&modified_response).map_err(actix_web::Error::from)?;

                        let response_length = response_body.len() as u64;

                        // Preserve original content-type header for verbose mode too
                        let mut response_builder = HttpResponse::build(status);
                        if let Some(content_type) = headers.get("content-type") {
                            response_builder.insert_header(("content-type", content_type.clone()));
                        } else {
                            response_builder.content_type("application/json");
                        }
                        insert_traceparent_header(&mut response_builder, &response_traceparent);

                        let service_response = ServiceResponse::new(request, response_builder.body(response_body)).map_into_right_body();

                        let mut durations = telemetry_data.read().await.durations.clone();
                        let is_error = status.is_client_error() || status.is_server_error();
                        let labels_refs = labels_to_refs(&labels_clone);
                        let active_labels_refs = labels_to_refs(&active_labels_clone);
                        metrics_clone.eden.complete_request_with_active_labels(
                            response_length,
                            end,
                            &mut durations,
                            is_error,
                            &labels_refs,
                            &active_labels_refs,
                        );
                        telemetry_data.write().await.durations = durations;

                        Ok(service_response)
                    } else {
                        // Normal mode - Track response size
                        let response_length = body_size_or_content_length(&body, &headers);

                        let mut durations = {
                            let mut tw = telemetry_data.write().await;
                            tw.set_eden_request_end(end);
                            tw.mut_labels(|labels| {
                                labels.set_http_status(status);
                                // Note: trace IDs already set in async_block initialization
                            });
                            tw.durations.clone()
                        };

                        let is_error = status.is_client_error() || status.is_server_error();
                        let labels_refs = labels_to_refs(&labels_clone);
                        let active_labels_refs = labels_to_refs(&active_labels_clone);
                        metrics_clone.eden.complete_request_with_active_labels(
                            response_length,
                            end,
                            &mut durations,
                            is_error,
                            &labels_refs,
                            &active_labels_refs,
                        );
                        log_trace!(
                            _ctx.clone(),
                            "Metrics -- final durations",
                            audience = eden_logger_internal::LogAudience::Internal,
                            durations = format!("{:?}", durations)
                        );
                        telemetry_data.write().await.durations = durations;

                        // Reconstruct response while preserving all original headers
                        let mut response_builder = HttpResponse::build(status);
                        for (name, value) in headers.iter() {
                            response_builder.insert_header((name.clone(), value.clone()));
                        }
                        insert_traceparent_header(&mut response_builder, &response_traceparent);
                        let http_response = response_builder.body(body);
                        let service_response = ServiceResponse::new(request, http_response).map_into_right_body();

                        Ok(service_response)
                    }
                }
                Err(err) => {
                    log_trace!(_ctx, "Metrics -- service response error", audience = eden_logger_internal::LogAudience::Internal);
                    let endpoint_end = Utc::now();
                    let end = endpoint_end;

                    let (labels_clone, mut durations) = {
                        let mut tw = telemetry_data.write().await;
                        tw.set_eden_request_end(end);
                        tw.mut_labels(|labels| {
                            labels.set_http_status(StatusCode::INTERNAL_SERVER_ERROR);
                            // Note: trace IDs already set in async_block initialization
                        });

                        // Set endpoint duration if not already set
                        if tw.durations.get_endpoint_duration().is_none() {
                            tw.durations.set_endpoint_request(endpoint_start);
                            tw.durations.set_endpoint_response(endpoint_end);
                        }

                        (tw.labels_low_cardinality(), tw.durations.clone())
                    };

                    let labels_refs = labels_to_refs(&labels_clone);
                    let active_labels_refs = labels_to_refs(&active_labels_clone);
                    metrics_clone.eden.complete_request_with_active_labels(0, end, &mut durations, true, &labels_refs, &active_labels_refs);
                    telemetry_data.write().await.durations = durations;
                    // EndpointGuard will drop here, automatically recording all metrics even on error

                    Err(err)
                }
            }
        })
    }
}

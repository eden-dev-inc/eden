/// Middleware struct that will be used to wrap the service
#[derive(Clone, Default)]
pub struct MetricsMiddleware {}

pub struct MetricsMiddlewareService<S> {
    service: S,
}
impl<S, B> MetricsMiddlewareService<S>
where
    S: actix_web::dev::Service<actix_web::dev::ServiceRequest, Response = actix_web::dev::ServiceResponse<B>, Error = actix_web::Error>,
    S::Future: 'static,
    B: 'static,
{
    fn start_request(
        &self,
        req: &actix_web::dev::ServiceRequest,
        metrics: Data<AllMetrics>,
    ) -> (DateTime<Utc>, TelemetryWrapper, Username, u64) {
        let start = Utc::now();
        let eden_node_uuid = if let Some(server_data) = req.app_data::<Data<ServerData>>() {
            server_data.public_key.clone()
        } else {
            EdenNodeUuid::new_uuid()
        };
        let metadata = req.extensions().get::<MetadataMapWrapper>().cloned().unwrap_or_else(|| {
            // Extract trace context from HTTP headers (traceparent, tracestate, etc.)
            use crate::metadata_map_from_http_headers;
            metadata_map_from_http_headers(req.headers()).into()
        });

        // Store metadata in request extensions for handlers to access via #[with_telemetry] macro
        req.extensions_mut().insert(metadata.clone());

        let mut labels = req.extensions().get::<TelemetryLabels>().cloned().unwrap_or_else(|| TelemetryLabels::new(&eden_node_uuid));

        // Populate HTTP headers from metadata for metrics labels
        labels.populate_from_metadata(metadata.metadata());
        let durations = req.extensions().get::<TelemetryDurations>().cloned().unwrap_or_default();

        let mut telemetry_wrapper = TelemetryWrapper::new_with_telemetry(
            TraceContext::from(metadata.metadata().clone()),
            metrics.clone().into_inner(),
            labels,
            durations,
        );

        // Set eden request start time
        telemetry_wrapper.set_eden_request_start(start);

        telemetry_wrapper.mut_labels(|labels| {
            labels.set_http_path(req.path());
            labels.set_http_method(req.method());
        });

        // Add user and org context if available
        let user;
        if let Some(auth) = req.extensions().get::<web::ReqData<ParsedJwt>>() {
            telemetry_wrapper.mut_labels(|labels| {
                labels.set_jwt(auth.clone().into_inner());
            });
            user = Username::new(auth.org_uuid().clone(), auth.user_id().clone());
        } else {
            user = Username::default()
        }

        // Track request size
        let request_length =
            req.headers().get("content-length").and_then(|h| h.to_str().ok()).and_then(|v| v.parse::<u64>().ok()).unwrap_or(0);

        (start, telemetry_wrapper, user, request_length)
    }
}


fn insert_traceparent_header(response_builder: &mut actix_web::HttpResponseBuilder, traceparent: &str) {
    if !traceparent.is_empty() {
        response_builder.insert_header(("traceparent", traceparent.to_string()));
    }
}

fn body_size_or_content_length<B: actix_web::body::MessageBody>(body: &B, headers: &actix_web::http::header::HeaderMap) -> u64 {
    if let BodySize::Sized(size) = body.size() {
        return size;
    }
    headers.get("content-length").and_then(|h| h.to_str().ok()).and_then(|v| v.parse::<u64>().ok()).unwrap_or(0)
}


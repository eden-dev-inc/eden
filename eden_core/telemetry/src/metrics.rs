/// Default eviction threshold: series inactive for this many export cycles are evicted.
const DEFAULT_EVICTION_THRESHOLD: u32 = 6;

/// State for delta temporality exports (required for Datadog).
///
/// Tracks previous values to compute deltas between export intervals.
/// Eviction is now handled internally by each dynamic metric type using
/// access-timestamp tracking.
pub struct FastMetricsExportState {
    pub eden: EdenCoreMetricsDogStatsDState,
    pub iam: IamMetricsDogStatsDState,
    pub endpoint: EndpointMetricsDogStatsDState,
    pub analytics: AnalyticsMetricsDogStatsDState,
    pub metadata: MetadataMetricsDogStatsDState,
    pub proxy: ProxyMetricsDogStatsDState,
    pub snapshot: SnapshotMetricsDogStatsDState,
    pub workload: WorkloadMetricsDogStatsDState,
    pub validator: ValidatorMetricsDogStatsDState,
    /// Eviction threshold: series inactive for this many cycles are evicted.
    pub eviction_threshold: u32,
    /// Previous cumulative value for span sampling delta export.
    prev_spans_recorded: u64,
    prev_spans_sampled_out: u64,
}

#[derive(Debug, Clone, Copy, Default)]
pub struct DynamicSeriesCardinalitySnapshot {
    pub eden: usize,
    pub iam: usize,
    pub endpoint: usize,
    pub analytics: usize,
    pub metadata: usize,
    pub proxy: usize,
    pub snapshot: usize,
    pub workload: usize,
    pub validator: usize,
}

impl DynamicSeriesCardinalitySnapshot {
    pub fn total(&self) -> usize {
        self.eden + self.iam + self.endpoint + self.analytics + self.metadata + self.proxy + self.snapshot + self.workload + self.validator
    }
}

impl FastMetricsExportState {
    pub fn new() -> Self {
        Self::with_eviction_threshold(DEFAULT_EVICTION_THRESHOLD)
    }

    /// Create export state with custom eviction threshold.
    ///
    /// `threshold` is the number of consecutive export cycles a series must be
    /// inactive before being evicted.
    pub fn with_eviction_threshold(threshold: u32) -> Self {
        Self {
            eden: EdenCoreMetricsDogStatsDState::new(),
            iam: IamMetricsDogStatsDState::new(),
            endpoint: EndpointMetricsDogStatsDState::new(),
            analytics: AnalyticsMetricsDogStatsDState::new(),
            metadata: MetadataMetricsDogStatsDState::new(),
            proxy: ProxyMetricsDogStatsDState::new(),
            snapshot: SnapshotMetricsDogStatsDState::new(),
            workload: WorkloadMetricsDogStatsDState::new(),
            validator: ValidatorMetricsDogStatsDState::new(),
            eviction_threshold: threshold,
            prev_spans_recorded: 0,
            prev_spans_sampled_out: 0,
        }
    }
    /// Total number of dynamic label sets retained in delta-export state maps.
    pub fn tracked_label_sets(&self) -> usize {
        self.eden.tracked_label_sets()
            + self.iam.tracked_label_sets()
            + self.endpoint.tracked_label_sets()
            + self.analytics.tracked_label_sets()
            + self.metadata.tracked_label_sets()
            + self.proxy.tracked_label_sets()
            + self.snapshot.tracked_label_sets()
            + self.workload.tracked_label_sets()
            + self.validator.tracked_label_sets()
    }
}

impl Default for FastMetricsExportState {
    fn default() -> Self {
        Self::new()
    }
}

